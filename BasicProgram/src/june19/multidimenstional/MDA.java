package june19.multidimenstional;

public class MDA {
	
	public static void main(String[] args) {
		int [] [] mat = { {2,3,5},{6,7,8},{2,4,8}};
		for(int i = 0; i<mat.length;i++)
		{
			for(int j = 0; j<mat[0].length;j++) {
				if(mat[i][j]%2==0) {
					System.out.print(mat[i][j] +" ");
				}
			}
//			
			System.out.println( " ");
	}
	}
	
	}

